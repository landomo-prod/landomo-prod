// ============================================================================
// Core Property Types
// ============================================================================

export interface PropertyCoordinates {
  lat: number;
  lng: number;
}

export interface PropertyFeature {
  id: string;
  name: string;
  icon: string; // Lucide icon name
}

export interface PriceHistoryEntry {
  date: string; // ISO date string (YYYY-MM-DD)
  price: number;
}

/**
 * Property interface - extends backend PropertyResult with computed fields
 */
export interface Property extends PropertyResult {
  // Computed fields (generated by adaptProperty)
  coordinates?: PropertyCoordinates;
  features?: PropertyFeature[];
  pricePerSqm?: number;
  priceHistory?: PriceHistoryEntry[];

  // Agent info (structured from flat fields)
  agent?: {
    name: string;
    phone: string;
    email: string;
    photo?: string;
  };
}

