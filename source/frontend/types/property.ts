/**
 * Converts backend PropertyResult to frontend Property with computed fields
 */
export function adaptProperty(result: PropertyResult): Property {
  // Calculate price per sqm (skip for "na dotaz" listings)
  const pricePerSqm = result.price > 1 && result.sqm && result.sqm > 0
    ? Math.round(result.price / result.sqm)
    : undefined;

  // Parse string lat/lon to numbers (PostgreSQL NUMERIC returns strings)
  const lat = typeof result.latitude === 'string' ? parseFloat(result.latitude) : result.latitude;
  const lon = typeof result.longitude === 'string' ? parseFloat(result.longitude) : result.longitude;

  // Build coordinates
  const coordinates: PropertyCoordinates | undefined =
    lat !== undefined && lon !== undefined && !isNaN(lat) && !isNaN(lon)
      ? { lat, lng: lon }
      : undefined;

  // Build features from amenities
  const features: PropertyFeature[] = [];
  if (result.has_parking) features.push({ id: 'parking', name: 'Parking', icon: 'parking-circle' });
  if (result.has_balcony) features.push({ id: 'balcony', name: 'Balcony', icon: 'snowflake' });
  if (result.has_terrace) features.push({ id: 'terrace', name: 'Terrace', icon: 'sun' });
  if (result.has_garden) features.push({ id: 'garden', name: 'Garden', icon: 'tree-deciduous' });
  if (result.has_elevator) features.push({ id: 'elevator', name: 'Elevator', icon: 'move-vertical' });
  if (result.has_garage) features.push({ id: 'garage', name: 'Garage', icon: 'warehouse' });
  if (result.has_pool) features.push({ id: 'pool', name: 'Pool', icon: 'waves' });
  if (result.has_basement) features.push({ id: 'basement', name: 'Basement', icon: 'box' });

  // Build agent object (show when any contact info exists)
  const agent = (result.agent_name || result.agent_phone || result.agent_email) ? {
    name: result.agent_name || '',
    phone: result.agent_phone || '',
    email: result.agent_email || '',
  } : undefined;

  // Sanitize image URLs — encode pipe characters that cause 400 errors on sdn.cz
  // Filter out Matterport URLs (3D tour links, not actual images)
  const images = result.images
    ?.filter(url => !url.includes('matterport.com'))
    .map(url => url.replace(/\|/g, '%7C'));

  // Normalize "m2" → "m²" in titles
  const title = result.title?.replace(/(\d)\s*m2\b/g, '$1 m²');

  // Normalize currency variants to standard code
  const currency = result.currency === 'Kc/mes.' ? 'CZK' : result.currency;

  return {
    ...result,
    title,
    images,
    currency,
    latitude: lat,
    longitude: lon,
    // Computed fields
    pricePerSqm,
    coordinates,
    features,
    agent,
  };
}

/**
 * Batch adapter for multiple properties
 */
export function adaptProperties(results: PropertyResult[]): Property[] {
  return results.map(adaptProperty);
}

