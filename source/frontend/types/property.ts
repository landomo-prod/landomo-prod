/**
 * Get display address from property (uses title field)
 * For Czech properties, title usually contains disposition and area
 * Example: "Prodej bytu 3+kk 78 m²"
 */
export function getPropertyAddress(property: Property): string {
  return property.title || property.city;
}

/**
 * Extract short address without disposition/area (Czech-specific)
 * "Prodej bytu 3+kk 78 m², Praha 2" → "Praha 2"
 */
export function getShortAddress(property: Property): string {
  // For Czech properties, city is usually cleaner than title
  return property.city || property.title;
}

/**
 * Get floor display (e.g., "3rd floor", "Ground floor")
 */
export function getFloorDisplay(floor?: number): string | undefined {
  if (floor === undefined) return undefined;
  if (floor === 0) return 'Ground';
  if (floor === 1) return '1st';
  if (floor === 2) return '2nd';
  if (floor === 3) return '3rd';
  return `${floor}th`;
}

/**
 * Format price with currency symbol
 */
export function isPriceOnRequest(price: number): boolean {
  return price <= 1;
}

export function formatPrice(price: number, currency: string = 'CZK'): string {
  if (isPriceOnRequest(price)) return 'na dotaz';

  if (currency === 'CZK') {
    return `${price.toLocaleString("cs-CZ")} Kč`;
  }
  const currencySymbol = currency === 'EUR' ? '€'
    : currency === 'USD' ? '$'
    : currency === 'GBP' ? '£'
    : currency;

  return `${currencySymbol}${price.toLocaleString()}`;
}

/**
 * Format area in square meters
 */
