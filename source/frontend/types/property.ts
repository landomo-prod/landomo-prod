/**
 * Format bedrooms from disposition (Czech)
 * Examples: "3+kk" → 3 bedrooms, "2+1" → 2 bedrooms
 */
export function getBedroomsFromDisposition(disposition?: string): number | undefined {
  if (!disposition) return undefined;

  const match = disposition.match(/^(\d+)/);
  return match ? parseInt(match[1], 10) : undefined;
}
