  // Location
  city?: string;
  region?: string;
  country?: string;

  // Size
  sqm_min?: number;
  sqm_max?: number;
  bedrooms_min?: number;
  bedrooms_max?: number;
  bathrooms_min?: number;

  // Amenities
  has_parking?: boolean;
  has_balcony?: boolean;
  has_elevator?: boolean;
  has_garden?: boolean;
  has_garage?: boolean;
  has_pool?: boolean;
  has_terrace?: boolean;
  has_basement?: boolean;

  // Czech-specific
  disposition?: string;
  ownership?: string;
  building_type?: string;
  condition?: string;

  // UK-specific
  tenure?: string;
  epc_rating?: string | string[];

